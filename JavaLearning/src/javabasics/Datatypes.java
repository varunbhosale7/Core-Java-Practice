package javabasics;


//data types specify the type of data that can be stored inside variable
//8 Primitive Data Types:
//boolean,byte,short,int,long,double,float,char,String
public class Datatypes {

	public static void main(String[] args) {
		
		boolean flag = true;
	    System.out.println(flag);    // prints true
	    
	    byte range1;
	    range1 = 124;
	    System.out.println(range1);    // prints 124
	    
	    short temperature;
	    temperature = -200;
	    System.out.println(temperature);  // prints -200
	    
	    int range2 = -4250000;
	    System.out.println(range2);  // print -4250000
	    
	    long range = -42332200000L;
	    System.out.println(range);    // prints -42332200000
	    
	    double number1 = -42.3;
	    System.out.println(number1);  // prints -42.3
	    
	    float number2 = -42.3f;
	    System.out.println(number2);  // prints -42.3
	    
	    char letter = 'q';
	    System.out.println(letter);  // prints q
	    
	    String myString = "Java Programming";
	    System.out.println(myString);
	}

}
