package conditionalStatements;

public class NestedIfElse {

	public static void main(String[] args) {
		
		    int i=19;
		    char ch='m';
		    
		    if(i>18)
		    {
		    	if(ch=='m')
		    	{
		    		System.out.println("Welcome");
		    	}
		    	else if(ch=='f')
		    	{
		    		System.out.println("Welcome");
		    	}
		    }
		    else
		    {
		    	System.out.println("Try Again");
		    }
	    
	    
	}

}
